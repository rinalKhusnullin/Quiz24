CREATE TABLE IF NOT EXISTS up_quiz_users
(
	ID INT NOT NULL AUTO_INCREMENT,
	NAME VARCHAR(64) NOT NULL,
	PASSWORD_HASH VARCHAR(128) NOT NULL,
	PRIMARY KEY(ID)
);

