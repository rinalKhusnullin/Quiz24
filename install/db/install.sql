
CREATE TABLE IF NOT EXISTS up_quiz_quizzes
(
	ID INT NOT NULL AUTO_INCREMENT,
	TITLE VARCHAR(256) NOT NULL,
	CODE VARCHAR(4) NOT NULL,
    IS_ACTIVE TINYINT(1) DEFAULT 0,
	USER_ID INT NOT NULL,
	PRIMARY KEY(ID),
	FOREIGN KEY (USER_ID)
		REFERENCES b_user(ID)
		ON DELETE CASCADE
);

