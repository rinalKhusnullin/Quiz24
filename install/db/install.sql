CREATE TABLE IF NOT EXISTS up_quiz_answers
(
	ID INT NOT NULL AUTO_INCREMENT,
	QUESTION_ID INT NOT NULL,
	ANSWER varchar(128),
	PRIMARY KEY(ID),
	FOREIGN KEY (QUESTION_ID)
		REFERENCES up_quiz_questions(ID)
		ON DELETE CASCADE
);

